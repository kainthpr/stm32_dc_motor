void motor(int speed, int dir){
	if (!dir){
		HAL_GPIO_WritePin(motor_dir1_GPIO_Port, motor_dir1_Pin, 1);
		HAL_GPIO_WritePin(motor_dir2_GPIO_Port, motor_dir2_Pin, 0);
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
	}
	else{
		HAL_GPIO_WritePin(motor_dir1_GPIO_Port, motor_dir1_Pin, 0);
		HAL_GPIO_WritePin(motor_dir2_GPIO_Port, motor_dir2_Pin, 1);
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
	}
	__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_2, speed); // set duty cycle 0->101 using CCR register
}

int map50_100(float pwm){ // -1 to 1 to 50 to 100. does not take dir into account.
	if (pwm < 0){ // -ve dir
		pwm = pwm * -1;
	}
	if (pwm > 1){ // maxed
		pwm = 1;
	}
	return (pwm+1)*50;
}

void x_task(){
	int error = set_loc - location;
	float prop = error*kp;

//	uint32_t cur_time = HAL_GetTick();
//	uint32_t elasped_time = cur_time - last_time;
	cumulativeError += error*ki;
	if (cumulativeError > 1){
		cumulativeError = 1;
	}
	if (cumulativeError < -1){
		cumulativeError = -1;
	}

	float derivative = (error - previous_error)*kd;
	float pwm = prop + cumulativeError + derivative;

//	last_time = cur_time;
	previous_error = error;
	if (error == 0) // completely kill
	{
		motor(0, 0);
	}
	if (pwm < 0){
		motor(map50_100(pwm), 0); // left
	}
	else{
		motor(map50_100(pwm), 1); // right
	}
//	int len = snprintf(UserTxBufferHS, 100, "p=%f,i=%f,d=%f,pg=%f,ig=%f,dg=%f,e=%d,pwm=%f\n",prop, cumulativeError, derivative, kp, ki, kd, error, pwm);
//	CDC_Transmit_HS(UserTxBufferHS, len); // send it out
}


void decode_payload(){
	char type = UserRxBufferHS[0];
	if (type == 'm'){ // motor
		speed = strtol(UserRxBufferHS+1, (char **)NULL, 10); // speed in byte 1 onwards
	}
	else if (type == 'x'){
		set_loc = strtol(UserRxBufferHS+1, (char **)NULL, 10);
		start = 1;
	}
	else if (type == 'p'){
		kp = strtol(UserRxBufferHS+1, (char **)NULL, 10)/10000000.0; // prop gain
		int len = snprintf(UserTxBufferHS, 100, "pg=%f\n", kp);
		CDC_Transmit_HS(UserTxBufferHS, len); // send it out
	}
	else if (type == 'i'){
		ki = strtol(UserRxBufferHS+1, (char **)NULL, 10)/10000000.0; // int gain
		int len = snprintf(UserTxBufferHS, 100, "ig=%f\n", ki);
		CDC_Transmit_HS(UserTxBufferHS, len); // send it out
	}
	else if (type == 'd'){
		kd = strtol(UserRxBufferHS+1, (char **)NULL, 10)/10000000.0; // deriv gain
		int len = snprintf(UserTxBufferHS, 100, "dg=%f\n", kd);
		CDC_Transmit_HS(UserTxBufferHS, len); // send it out
	}
}

